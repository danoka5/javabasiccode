package Loops;

public class ForLoop {

	public static void main(String[] args) {
	for (int i=10; i>0; i--) {
		if (i%2==0) {
			System.out.println(i);
		}
	}
	System.out.println("----------------");
	
for (int a=2; a<=14; a++) {
	if (a%2==0) {
		System.out.println(a);
	}
}
	System.out.println("-----------------");
	
for (int b=5; b<22; b++) {
	if (b%2==1) {
		System.out.println(b);
	}
}
	System.out.println("-----------------");
	
for (int c=1; c<=4; c++) {
	System.out.println("Hello");
}

	System.out.println("----------------");
	
for (int d=2011; d<=2019; d++) {
	System.out.println("Hey it is year: " +d);
}

System.out.println("----------------------");

	
	}
}

